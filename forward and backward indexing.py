l1=[10,20,30,40,50]

print("List: [10,20,30,40,50]")

print("Forward Indexing:")
print(l1[2])
print(l1[2:4])
print(l1[1:4:2])
print(l1[2:])
print(l1[:2])
print(l1[:])
print(l1[::2])

print("Backward Indexing:")
print(l1[-3])
print(l1[-3:-1])
print(l1[-3:])
print(l1[:-3])
print(l1[:])
print(l1[::2])

